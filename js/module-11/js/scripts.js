const handleSizeForm = (evt) => {

  evt.preventDefault();

  
  Array.from(checkboxes).reduce((acc, elem) => {
    const name = elem.getAttribute('name');
    const ternNumb = name == 'color' ? elem.value : Number(elem.value);
    elem.checked ? filter[name].push(ternNumb) : acc;
    console.log(name);
  }, '');
  

  let filtered = Object.keys(filter).reduce((acc, key) => {
    if(filter[key].length) {
      return  acc.filter(elem => filter[key].includes(elem[key]))
    }
    return acc;
  }, laptops);
  

  const markup = filtered.reduce((acc, elem) => acc + template(elem),'');
  
  list.innerHTML = markup;
}

const clear = (e) => {
  window.location.reload();
}


